package Module1;

public class _5_SwitchCaseproblem {

	public static void main(String[] args) {
		int num = 3;
		char ch ='r';
		switch(ch)
		{
		case 1: System.out.println("hyd");
		break;
		
		case 2: System.out.println("kphb");
		break;
		
		case 3: System.out.println("kgm");
		break;
		
		case 'r': System.out.println("tnk");
		break;
		
		case 5: System.out.println("kpm");
		break;
		default : System.out.println("no one above");
		}
		

	}

}
